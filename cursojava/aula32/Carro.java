package com.natanael.cursojava.aula32;

public class Carro {
	private String marca;
	private String modelo;
	private byte numPassageiros;
	private float capCombustivel;
	private float consumoCombustivel;
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public byte getNumPassageiros() {
		return numPassageiros;
	}
	public void setNumPassageiros(byte numPassageiros) {
		this.numPassageiros = numPassageiros;
	}
	public float getCapCombustivel() {
		return capCombustivel;
	}
	public void setCapCombustivel(float capCombustivel) {
		this.capCombustivel = capCombustivel;
	}
	public float getConsumoCombustivel() {
		return consumoCombustivel;
	}
	public void setConsumoCombustivel(float consumoCombustivel) {
		this.consumoCombustivel = consumoCombustivel;
	}
	
	
}
